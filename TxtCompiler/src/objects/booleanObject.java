package objects;

public class booleanObject
	{

private String name;
private boolean value;
public booleanObject(String n, boolean v){
	n = name;
	v = value;
}

public String getName()
	{
		return name;
	}
public void setName(String name)
	{
		this.name = name;
		
	}
public boolean getValue()
	{
		return value;
	}
public void setValue(boolean value)
	{
		this.value = value;
	}
		
		
		
	}
